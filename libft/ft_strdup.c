#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s_len;
	char	*dst;

	i = 0;
	s_len = ft_strlen(s1);
	dst = (char *)malloc((s_len + 1) * sizeof(char));
	if (!dst)
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
